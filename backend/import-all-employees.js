const importAllEmployees = async () => {
  await connectDB();

  try {
    // Get manager (use first global manager)
    const manager = await Manager.findOne({ siteId: null });
    if (!manager) {
      console.error('‚ùå No manager found. Please create a manager first.');
      process.exit(1);
    }
    console.log(`‚úÖ Using manager: ${manager.name} (${manager.email})\n`);

    // Get admin (or use manager)
    let admin = await Admin.findOne();
    if (!admin) {
      console.log('‚ö†Ô∏è  No admin found, using manager as createdBy');
      admin = manager;
    } else {
      console.log(`‚úÖ Using admin: ${admin.name} (${admin.email})\n`);
    }

    console.log(`üìù Starting import of ${allEmployees.length} employees...\n`);

    let successCount = 0;
    let skippedCount = 0;
    let errorCount = 0;

    // Distribute employees across shifts
    const shifts = ['morning', 'evening', 'night'];
    
    for (let i = 0; i < allEmployees.length; i++) {
      const name = allEmployees[i].trim();
      
      try {
        // Check if employee already exists
        const existing = await Employee.findOne({ 
          name: { $regex: new RegExp(`^${name.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}$`, 'i') }
        });

        if (existing) {
          skippedCount++;
          if (i % 50 === 0) {
            console.log(`‚è≠Ô∏è  Skipped ${skippedCount} existing employees so far...`);
          }
          continue;
        }

        // Distribute shifts evenly
        const shift = shifts[i % 3];

        const employee = new Employee({
          name: name,
          email: '',
          mobile: '',
          address: 'Not Provided',
          managerId: manager._id,
          shift: shift,
          createdBy: admin._id,
          isCreatedByAdmin: false,
          siteId: null // Global employee - visible to admin
        });

        await employee.save();
        successCount++;

        // Progress indicator
        if ((i + 1) % 50 === 0) {
          console.log(`‚úÖ Imported ${i + 1}/${allEmployees.length} employees...`);
        }
      } catch (error) {
        errorCount++;
        console.error(`‚ùå Error creating ${name}:`, error.message);
      }
    }

    console.log('\n' + '='.repeat(60));
    console.log('üìä IMPORT SUMMARY');
    console.log('='.repeat(60));
    console.log(`‚úÖ Successfully imported: ${successCount}`);
    console.log(`‚è≠Ô∏è  Skipped (already exist): ${skippedCount}`);
    console.log(`‚ùå Failed: ${errorCount}`);
    console.log(`üìã Total processed: ${allEmployees.length}\n`);

    // Verify
    const totalEmployees = await Employee.countDocuments();
    const globalEmployees = await Employee.countDocuments({ siteId: null });
    
    console.log(`üìä Total employees in database: ${totalEmployees}`);
    console.log(`üåç Global employees (visible to Admin): ${globalEmployees}\n`);

    if (successCount > 0) {
      console.log('‚úÖ Import completed! Refresh your frontend to see all employees.\n');
    }

  } catch (error) {
    console.error('‚ùå Import failed:', error);
  } finally {
    await mongoose.disconnect();
    console.log('‚úÖ Disconnected from MongoDB');
  }
};

